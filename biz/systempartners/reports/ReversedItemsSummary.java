    private boolean isInternalLPO(String order) {
        boolean internal = false;

        try {
            PreparedStatement pst = connectDB.prepareStatement("SELECT case when '" + order + "' in (select order_no from st_orders_delivery ) then true else false end as result");
            ResultSet rset = pst.executeQuery();
            while (rset.next()) {
                internal = rset.getBoolean(1);
            }

        } catch (SQLException ex) {
                        ex.printStackTrace();             //ex.printStackTrace();
        }

        return internal;
    }

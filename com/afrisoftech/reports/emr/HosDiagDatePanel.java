    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.setCursor(new java.awt.Cursor(javax.swing.JFrame.WAIT_CURSOR));


        String Categ = null;
        String ReportType = null;
        if (this.outpatientChkbx.isSelected()) {
            Categ = "OUT-Patient only";
        } else {
            if (this.inpatientChkbx.isSelected()) {
                Categ = "IN-Patient only";
            } else {
                Categ = "Both IN/OUT Patients";
            }
        }

        if (maleRdbtn.isSelected()) {
            ReportType = maleRdbtn.getText();
        } else if(femaleRdbtn.isSelected()) {
            ReportType = femaleRdbtn.getText();
        } else {
            ReportType = allGenderRdbtn.getText();
        }

        this.setCursor(new java.awt.Cursor(java.awt.Cursor.WAIT_CURSOR));
        com.afrisoftech.reports.emr.MOH718DiagnosisAnalysisPdf policy = new com.afrisoftech.reports.emr.MOH718DiagnosisAnalysisPdf();

        policy.MOH718DiagnosisAnalysisPdf(connectDB, reportStartDate, reportEndDate, ReportType, Categ);
        this.setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));



        // Add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed


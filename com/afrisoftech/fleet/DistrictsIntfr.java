    private void newActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newActionActionPerformed
         try {
            java.sql.PreparedStatement pstmt = connectDB.prepareStatement("insert into fleet_schema.district values(?,?,?)");
            if(districtidTxt.getText().equals("")){
                javax.swing.JOptionPane.showMessageDialog(this, "You must enter Country code","Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
            }else{
                pstmt.setString(1,districtidTxt.getText());
            }
            if(districtnameTxt.getText().equals("")){
                javax.swing.JOptionPane.showMessageDialog(this, "You must enter Country name","Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
            }else{
                pstmt.setString(2,districtnameTxt.getText());
                
            } pstmt.setString(3,CountrytCbx.getSelectedItem().toString()); 
            pstmt.executeUpdate();
            errorlbl.setForeground(java.awt.Color.blue);
            errorlbl.setText("Insert successful");
        }   catch(java.sql.SQLException sq){
            javax.swing.JOptionPane.showMessageDialog(this, sq.getMessage(),"Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
            
            System.out.println(sq.getMessage());
            errorlbl.setForeground(java.awt.Color.red);
            errorlbl.setText("Sorry. Another country Code already exists");
        }// Add your handling code here:
    }//GEN-LAST:event_newActionActionPerformed
   
    

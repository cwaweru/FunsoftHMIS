    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ////saving to st_td_collection
         try{
                    if(tendernotxt.getText()!=null||tendernotxt1.getText()!=null){
                      //INSERT INTO st_td_collection(supplier, receipt_no, collected)  VALUES (?, ?, ?);
                    java.sql.PreparedStatement td = connectDB.prepareStatement("INSERT INTO st_td_collection(supplier, receipt_no,date_collected,tender_no)  VALUES (?, ?, current_date, ?);");
                    
                    td.setObject(1, tendernotxt1.getText().toString());
                    td.setObject(2,"APPROVED PAID");
                    td.setObject(3, tendernotxt.getText().toString());
                    td.executeUpdate();
                    
                    JOptionPane.showMessageDialog(null,"The supplier "+tendernotxt1.getText().toString()+" Has Been Approved Not To Pay For a Tender Doc", "CONFIRMATION MESSAGE", JOptionPane.INFORMATION_MESSAGE);
                    }
         }
         catch(Exception efg){
         efg.printStackTrace();
         efg.getMessage();
         
         }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tenderrecordstxtCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tenderrecordstxtCaretUpdate
        // TODO add your handling code here:
        if (this.tenderrecordstxt.getCaretPosition() < 1) {
            System.out.print("Nothing");

        } else

        {
            tenderrecordstbl.setModel(com.afrisoftech.dbadmin.TableModel.createTableVectors(connectDB, "SELECT DISTINCT tender_no, quotation_date, buyer_details FROM st_item_to_quote where "
                + " tender_no ilike '%" +tenderrecordstxt.getText()+"%' order by quotation_date desc;"));
        tenderrecordstbl.setShowHorizontalLines(false);

        tenderrecordsspnl.setViewportView(tenderrecordstbl);

        }

    }//GEN-LAST:event_tenderrecordstxtCaretUpdate


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Boolean check_insert=false;
            // TODO add your handling code here:
            connectDB.setAutoCommit(false);
            if(sectionTbl.getValueAt(0,0)!=null){
            for(int i=0; i<sectionTbl.getRowCount(); i++){
                if(sectionTbl.getValueAt(i, 0)!=null){
            java.sql.PreparedStatement pstmt =connectDB.prepareStatement("INSERT INTO st_buyers_section(section_code, section_name) VALUES (?, ?)");
            pstmt.setObject(1,sectionTbl.getValueAt(i, 0));
            pstmt.setObject(2,sectionTbl.getValueAt(i, 1));
            pstmt.executeUpdate();
            
            check_insert=true;
        
            }
            }
            
            if(check_insert=true){
              connectDB.setAutoCommit(false);
              connectDB.commit();
                connectDB.setAutoCommit(true);
                JOptionPane.showMessageDialog(null,"SECTION SAVED SUCCESSFULLY", "CONFIRMATION  MESSAGE",JOptionPane.ERROR_MESSAGE);
            
            }
            }
            else{
                connectDB.setAutoCommit(false);
              connectDB.rollback();
                connectDB.setAutoCommit(true);
            JOptionPane.showMessageDialog(null,"you need to input a buyers section at the first row", "ERROR MESSAGE",JOptionPane.ERROR_MESSAGE);
            
            }
        }
        catch(Exception ds){
        ds.printStackTrace();
        ds.getMessage();
        
        }

    }//GEN-LAST:event_jButton1ActionPerformed


            connectDB.setAutoCommit(false);
            java.sql.PreparedStatement pstmt31 = connectDB.prepareStatement("DELETE from st_main_stores WHERE code = '" + centralStoresTable.getModel().getValueAt(centralStoresTable.getSelectedRow(), 0).toString() + "'");

            java.sql.Statement h = connectDB.createStatement();

            java.sql.ResultSet w = g.executeQuery("select concat( building_name,' ',floor_no,' ',street,' ', town ,',',country) , contact_person,tel1,tel2, concat(postal_address,' ,',town),email_address, concat(bank_account_no,':',bank_name,' -', bank_branch) from st_suppliers where supplier_name ilike '" + jTable3.getModel().getValueAt(jTable3.getSelectedRow(), 1).toString() + "'");

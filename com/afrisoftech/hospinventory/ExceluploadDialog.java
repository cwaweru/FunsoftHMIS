    private String getSupCode(String supplier) {
        String code = "-";
        try {
            PreparedStatement pst = connectDB.prepareStatement("select distinct code from st_suppliers where upper(supplier_name) ilike '" + supplier.trim() + "' limit 1");
            ResultSet rset = pst.executeQuery();
            while (rset.next()) {
                code = rset.getString(1);
            }

        } catch (SQLException ex) {
                        ex.printStackTrace();             //Exceptions.printStackTrace(ex);
        }

        return code;

    }

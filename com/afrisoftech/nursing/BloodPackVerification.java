    private void recordSignsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recordSignsBtnActionPerformed
        
        try{ String user =null;
              java.sql.Statement st112 = connectDB.createStatement();
                                java.sql.ResultSet rs112 = st112.executeQuery("SELECT CURRENT_USER");
                                while (rs112.next()) {


                  user= rs112.getObject(1).toString();

                            }
            
             connectDB.setAutoCommit(false);
             
            
            String packinsert="INSERT INTO nursing.blood_pack_verification(" +
"            pack_no, pack_details, blood_group, blood_rhesus, " +
"              done_by,  date_recorded,blood_type)" +
"    VALUES (?, ?, ?, ?, ?, localtimestamp,?);";
            
            java.sql.PreparedStatement pstmt = connectDB.prepareStatement(packinsert);
                pstmt.setObject(1,Packno.getSelectedItem());
                 pstmt.setObject(2,packdetails.getText());
                                                   
                 
                       if(bloodGroupUnknownRbtn.isSelected()){
                          pstmt.setObject(3,"Unknown");
                    }else
                     if(bloodGroupARbtn.isSelected()){
                          pstmt.setObject(3,"A");
                    }
                    else
                          if(bloodGroupBRbtn.isSelected()){
                          pstmt.setObject(3,"B");
                    }else
                               if(bloodGroupORbtn.isSelected()){
                          pstmt.setObject(3,"O");
                    }else
                                    if(bloodGroupABRbtn.isSelected()){
                          pstmt.setObject(3,"AB");
                    }
                       
                        if(rhesusUnknownRbtn.isSelected()){
                         pstmt.setObject(4,"Unknown");
                    } else
                        if(rhesusPositiveRbtn.isSelected()){
                         pstmt.setObject(4,"+");
                    } else
                            if(rhesusNegativeRbtn.isSelected()){
                         pstmt.setObject(4,"-");
                    } 
                       pstmt.setObject(5,user);
                        pstmt.setObject(6,bloodtype.getSelectedItem());
                       
                       
                pstmt.executeUpdate();
                
              
                javax.swing.JOptionPane.showMessageDialog(this,"Saved Successfully","",javax.swing.JOptionPane.INFORMATION_MESSAGE);
              
            
              connectDB.commit();
                                connectDB.setAutoCommit(true);
                
        }
        catch(Exception packinsert){
            javax.swing.JOptionPane.showMessageDialog(this,"Please Ensure All \n Fields marked * are filled","",javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println("Pack insert error is "+packinsert);
             try {
                                        connectDB.rollback();
                                    }catch (java.sql.SQLException sql){
                                        javax.swing.JOptionPane.showMessageDialog(this,sql.getMessage(),"Error Message!",javax.swing.JOptionPane.ERROR_MESSAGE);
                                    }
        }
    }//GEN-LAST:event_recordSignsBtnActionPerformed


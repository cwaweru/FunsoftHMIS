    public java.util.Vector getBeginEndDates() {

        dateStartEnd = new java.util.Vector(1, 1);

        dateStartEnd.addElement(beginDateSpinner.getValue().toString());

        dateStartEnd.addElement(endDateSpinner.getValue().toString());

        return dateStartEnd;

    }

    public void getReport(int reportName) {
        String StoreName = "";
        if (jRadioButton1.isSelected()) {
            StoreName = "SubStore";
        } else if (jRadioButton2.isSelected()) {
            StoreName = "MainStore";
        } else {
            StoreName = "All Stores";
        }
        switch (reportName) {

            case 22: {
                com.afrisoftech.hospinventory.DrugFlowPdf policy = new com.afrisoftech.hospinventory.DrugFlowPdf();

                policy.DrugFlowPdf(connectDB, this.beginDpckr.getDate(), this.endDpckr.getDate(), null, this.storeNamecmbx.getSelectedItem().toString(), null);

                //this.dispose();
            }
            break;

            case 8: {
                com.afrisoftech.hospinventory.StoresBalPdf policy = new com.afrisoftech.hospinventory.StoresBalPdf();
                if (!StoreName.contains("All Stores")) {
                    policy.StoresBalPdf(connectDB, this.beginDpckr.getDate().toString(), this.endDpckr.getDate().toString(), storeNamecmbx.getSelectedItem().toString(), StoreName.toString());
                } else {
                    policy.StoresBalPdf(connectDB, this.beginDpckr.getDate().toString(), this.endDpckr.getDate().toString(), "", StoreName.toString());
                }

            }
            break;

            case 29: {
                com.afrisoftech.hospinventory.MainStoresAuditPdf policy = new com.afrisoftech.hospinventory.MainStoresAuditPdf();

                policy.MainStoresAuditPdf(connectDB, this.beginDpckr.getDate(), this.endDpckr.getDate(), storeNamecmbx.getSelectedItem().toString());

                //this.dispose();
            }
            case 2014529: {
                if (!storeNamecmbx.getSelectedItem().toString().equalsIgnoreCase("-") || storeNamecmbx.getSelectedItem() == null) {
                    com.afrisoftech.hospinventory.StockRecSchPerStorePdf policy = new com.afrisoftech.hospinventory.StockRecSchPerStorePdf();

                    policy.StockRecSchPerStorePdf(connectDB, this.beginDpckr.getDate(), this.endDpckr.getDate(), storeNamecmbx.getSelectedItem().toString());
                }
                else{
                    JOptionPane.showMessageDialog(this, "No valid store selected");
                }
            }
            break;

            default:
                ;
        }
    }

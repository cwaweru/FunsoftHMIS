    public java.util.Vector getBeginEndDates() {
        
        dateStartEnd = new java.util.Vector(1,1);
        
        dateStartEnd.addElement(beginDateSpinner.getValue().toString());
        
        dateStartEnd.addElement(endDateSpinner.getValue().toString());
        
        return dateStartEnd;
        
    }
    
    public void getReport(int reportName, boolean printState) {
        
        String InvoiceType = null;
        if (jRadioButton1.isSelected()){
            InvoiceType = "OP";
        }else{
            if (jRadioButton2.isSelected()){
                InvoiceType = "IP";
            }else{
                if (jRadioButton3.isSelected()){
                    InvoiceType = "BOTH";
                }
            }
        }
        
        
        switch (reportName) {
            
            
           case 59:
                
            {
                com.afrisoftech.reports.FinalisedInvListPdf policy = new com.afrisoftech.reports.FinalisedInvListPdf();
                
                policy.FinalisedInvListPdf(connectDB, this.datePicker1.getDate(), this.datePicker2.getDate(),InvoiceType);
                
                
                this.dispose();
                
            } break;
            
            
            default :  ;
        }
    }
    

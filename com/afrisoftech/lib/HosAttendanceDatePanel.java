    public void getReport(int reportName) {

        //  int mycursor = 0;
        String Categ = null;
        if (this.newPatientsCbkx.isSelected()) {
            Categ = "New";
        } else {
            if (this.revisitingPatientsChkbx.isSelected()) {
                Categ = "Old";
            } else {
                if (this.allPatientsChkbx.isSelected()) {
                    Categ = "All";
                }
            }
        }
        switch (reportName) {

            case 43: {
                com.afrisoftech.reports.PatientsAttSheetPdf policy = new com.afrisoftech.reports.PatientsAttSheetPdf();

                policy.PatientsAttSheetPdf(connectDB, this.startDatePicker.getDate(), this.endDatePicker.getDate(), Categ, clinicCmbx.getSelectedItem() != null ? clinicCmbx.getSelectedItem().toString() : "-", paymentModeCmbx.getSelectedItem().toString(), schemeNameCmbx.getSelectedItem());

            }

            break;

            case 204: {
                com.afrisoftech.records.reports.OutPatientStatementPdf policy = new com.afrisoftech.records.reports.OutPatientStatementPdf();

                policy.OutPatientStatementPdf(connectDB, this.startDatePicker.getDate(), this.endDatePicker.getDate(), clinicCmbx.getSelectedItem(), paymentModeCmbx.getSelectedItem().toString(), schemeNameCmbx.getSelectedItem());

                // com.afrisoftech.reports.OpAttSheetSummPdf policy = new com.afrisoftech.reports.OpAttSheetSummPdf();
                //policy.OpAttSheetSummPdf(connectDB, this.datePicker1.getDate(), this.datePicker2.getDate());
                //this.dispose();
            }
            break;

            default:
                ;
        }
    }

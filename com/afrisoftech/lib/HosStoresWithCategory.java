    public java.util.Vector getBeginEndDates() {
        
        dateStartEnd = new java.util.Vector(1,1);
        
        dateStartEnd.addElement(beginDateSpinner.getValue().toString());
        
        dateStartEnd.addElement(endDateSpinner.getValue().toString());
        
        return dateStartEnd;
        
    }
    
    public void getReport(int reportName) {
        String StoreName = "";
        if(jRadioButton1.isSelected()){
            StoreName = "SubStore";
        }else{
            StoreName = "MainStore";
        }
        switch (reportName) {
            
            case 22:
                
            {
                com.afrisoftech.hospinventory.DrugFlowPdf policy = new com.afrisoftech.hospinventory.DrugFlowPdf();
                
                policy.DrugFlowPdf(connectDB, this.datePicker1.getDate(), this.datePicker2.getDate(),null,this.jComboBox1.getSelectedItem().toString(),null);
                
                
                //this.dispose();
                
            } break;
            
            case 8:
                
            {
                com.afrisoftech.hospinventory.StoresBalPerCategoryPdf policy = new com.afrisoftech.hospinventory.StoresBalPerCategoryPdf();
                
                policy.StoresBalPerCategoryPdf(connectDB, this.datePicker1.getDate().toString(), this.datePicker2.getDate().toString(),jComboBox1.getSelectedItem().toString(),StoreName.toString(),((String)jComboBox2.getSelectedItem()));
                
                 
            } break;
            
            case 29:
                
            {
                com.afrisoftech.hospinventory.MainStoresAuditPdf policy = new com.afrisoftech.hospinventory.MainStoresAuditPdf();
                
                policy.MainStoresAuditPdf(connectDB, this.datePicker1.getDate(), this.datePicker2.getDate(),jComboBox1.getSelectedItem().toString());
                
                
                //this.dispose();
                
            } break;
            
            
            default :  ;
        }
    }
    

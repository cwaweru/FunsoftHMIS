    public java.util.Vector getBeginEndDates() {

        dateStartEnd = new java.util.Vector(1, 1);

        dateStartEnd.addElement(beginDateSpinner.getValue().toString());

        dateStartEnd.addElement(endDateSpinner.getValue().toString());

        return dateStartEnd;

    }

    public void getReport(int reportName) {

        switch (reportName) {

            case 52502: {
                if (jCheckBox2.isSelected()) {
                    com.afrisoftech.hospinventory.ProcuremenrReqPdf_ policy = new com.afrisoftech.hospinventory.ProcuremenrReqPdf_();

                    policy.ProcuremenrReqPdf(connectDB, deptTxt.getText(), reqNotxt.getText());

                } else {
                    com.afrisoftech.hospinventory.SRQReqPdf policy = new com.afrisoftech.hospinventory.SRQReqPdf();

                    policy.SRQReqPdf(connectDB, deptTxt.getText(), reqNotxt.getText());
                }

                this.dispose();

            }
       break;
            
            case 117: {

                com.afrisoftech.hospinventory.InternalRequisitionPDF policy = new com.afrisoftech.hospinventory.InternalRequisitionPDF();

                policy.InternalRequisitionPDF(connectDB, deptTxt.getText(), deptTxt.getText(), reqNotxt.getText(), UserName.getLoginName(connectDB).toLowerCase());

            }
            break;

          //  default:  ;
        }
        
          this.dispose();
    }


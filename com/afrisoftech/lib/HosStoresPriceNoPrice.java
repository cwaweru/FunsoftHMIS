    public java.util.Vector getBeginEndDates() {

        dateStartEnd = new java.util.Vector(1, 1);

        dateStartEnd.addElement(beginDateSpinner.getValue().toString());

        dateStartEnd.addElement(endDateSpinner.getValue().toString());

        return dateStartEnd;

    }

    public void getReport(int reportName) {

        switch (reportName) {
 

 //case 25: {
            case 5275: {
                if (centralStrsrbtn.isSelected()) {
                    if (jCheckBox1.isSelected()) {
                        com.afrisoftech.hospinventory.StockItemsPdf policy = new com.afrisoftech.hospinventory.StockItemsPdf();

                        policy.StockItemsPdf(connectDB, jComboBox1.getSelectedItem().toString());
                    } else {
                        com.afrisoftech.hospinventory.StockItemsWithoutPricePdf policy = new com.afrisoftech.hospinventory.StockItemsWithoutPricePdf();

                        policy.StockItemsWithoutPricePdf(connectDB, jComboBox1.getSelectedItem().toString());

                    }

                    //  this.dispose();
                } else {
                    if (jCheckBox1.isSelected()) {
                        com.afrisoftech.hospinventory.StockPricesWithPricePdf policy = new com.afrisoftech.hospinventory.StockPricesWithPricePdf();

                        policy.StockPricesWithPricePdf(connectDB, jComboBox1.getSelectedItem().toString());
                    } else {
                        com.afrisoftech.hospinventory.StockPricesWithMarkupPricePdf policy = new com.afrisoftech.hospinventory.StockPricesWithMarkupPricePdf();

                        policy.StockPricesWithMarkupPricePdf(connectDB, jComboBox1.getSelectedItem().toString());

                    }
                }
            }
            break;


            default:
                ;
        }
    }

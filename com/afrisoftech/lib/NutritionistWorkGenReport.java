    public java.util.Vector getBeginEndDates() {

        dateStartEnd = new java.util.Vector(1, 1);

        dateStartEnd.addElement(beginDateSpinner.getValue().toString());

        dateStartEnd.addElement(endDateSpinner.getValue().toString());

        return dateStartEnd;

    }

    public void getReport(int reportName) {

        switch (reportName) {
            case 6666: {
                if (summarizedRadioBtn.isSelected()) {
                        com.afrisoftech.hospinventory.NutritionistSummReportsPdf policy = new com.afrisoftech.hospinventory.NutritionistSummReportsPdf();

                        policy.NutritionistReportItemsPdf(connectDB, this.nutritionistBDdatePicker.getDate(), this.nutritionistEDdatePicker.getDate(),nutritionistNamCbx.getSelectedItem().toString());
                        
                        this.dispose();
                     
                } else if (detailedRadioBtn.isSelected()) {
                        com.afrisoftech.hospinventory.NutritionistDailyReportsPdf policy = new com.afrisoftech.hospinventory.NutritionistDailyReportsPdf();

                        policy.NutritionistDailyReportItemsPdf(connectDB, this.nutritionistBDdatePicker.getDate(), this.nutritionistEDdatePicker.getDate(),nutritionistNamCbx.getSelectedItem().toString());
                        
                        this.dispose();
                     
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Option Button not selected", "Select option Button", JOptionPane.ERROR_MESSAGE);
                    
                }
                
            }
                
        }
        
    }
    
    

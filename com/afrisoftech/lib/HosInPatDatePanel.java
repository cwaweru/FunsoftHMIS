    private void ipNumberstblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ipNumberstblMouseClicked

        invoicenoTxt.setText(ipNumberstbl.getValueAt(ipNumberstbl.getSelectedRow(), 0).toString());
        patientNotxt.setText(ipNumberstbl.getValueAt(ipNumberstbl.getSelectedRow(), 1).toString());

        searchIPdialog.dispose();        // Add your handling code here:
        schemeCmbx.setModel(com.afrisoftech.lib.ComboBoxModel.ComboBoxModel(connectDB, "SELECT DISTINCT upper(dealer) from ac_debtors where invoice_no='"+invoicenoTxt.getText()+"' AND admission_no='"+patientNotxt.getText()+"'"));
        schemeCmbx.setSelectedItem("NHIF");
        try {
            java.sql.Statement stDate = connectDB.createStatement();
            java.sql.ResultSet rsetDate = stDate.executeQuery("select DISTINCT date_admitted::date,discharge_date::date from hp_admission where ( invoice_no ILIKE '%"+searchIPtxt.getText().toString()+"%'  or patient_no ilike  '%"+searchIPtxt.getText().toString()+"%') ");
            
            while(rsetDate.next()){
                admdatetxt.setDate(rsetDate.getDate(1));
                dischargeDt.setDate(rsetDate.getDate(2));
                
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(HosInPatDatePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ipNumberstblMouseClicked
    

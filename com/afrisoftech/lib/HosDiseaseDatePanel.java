    public java.util.Vector getBeginEndDates() {
        
        dateStartEnd = new java.util.Vector(1,1);
        
        dateStartEnd.addElement(beginDateSpinner.getValue().toString());
        
        dateStartEnd.addElement(endDateSpinner.getValue().toString());
        
        return dateStartEnd;
        
    }
    
    public void getReport(int reportName) {
        
        //  int mycursor = 0;
        
        switch (reportName) {
            
            
            case 585683:
                String Categ = null;
                if(this.jCheckBox1.isSelected()){
                    Categ = this.jCheckBox1.getText();
                }else{
                    if(this.jCheckBox2.isSelected()){
                        Categ = this.jCheckBox2.getText();
                    }else{
                        Categ = this.jCheckBox3.getText();
                    }
                }
                {
                    
                    com.afrisoftech.records.reports.DiseaseAnalysisPdf policy = new com.afrisoftech.records.reports.DiseaseAnalysisPdf();
                    
                    policy.DiseaseAnalysisPdf(connectDB, this.datePicker1.getDate(), this.datePicker2.getDate(),Categ, jTextField36.getText());
                    
                    
                    //this.dispose();
                    
                } break;
                
                
     
            default :  ;
        }
    }
    

    public java.util.Vector getBeginEndDates() {

        dateStartEnd = new java.util.Vector(1, 1);

        dateStartEnd.addElement(beginDateSpinner.getValue().toString());

        dateStartEnd.addElement(endDateSpinner.getValue().toString());

        return dateStartEnd;

    }

    public void getReport(int reportName) {

        switch (reportName) {
            case 4444: {
                com.afrisoftech.hospinventory.WardReportsPdf policy = new com.afrisoftech.hospinventory.WardReportsPdf();
                
                policy.WardItemsPdf(connectDB, this.wardNutNameBDdatePicker.getDate(), this.wardNutNameEDdatePicker.getDate(),wardNutNamCbx.getSelectedItem().toString());
                
                this.dispose(); 
                
                
            }
        }
           
        
    }
    

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String time =null;
        if(storeNamecmbx.getSelectedItem()==null){
            javax.swing.JOptionPane.showMessageDialog(null, "Please specify the store name before you attempt to Update.");
        }
        else{
           try {
               
               
               
           PreparedStatement updsubStores1 = connectDB.prepareStatement("SELECT DISTINCT  trans_date::time FROM st_sub_stores where  transaction_no ilike 'Stock%' and initcap(store_name) = '"+storeNamecmbx.getSelectedItem().toString()+"' and trans_date::date ='"+dateCmbx.getSelectedItem().toString()+"' ");
            ResultSet rset = updsubStores1.executeQuery();
            while(rset.next()){
            
                time =(rset.getString(1));
            
            }
               
               
               
               
               
            PreparedStatement updsubStores = connectDB.prepareStatement("update st_sub_stores set trans_date='"+SQLDateFormat.getSQLDate(datePicker2.getDate())+"|| "+time+"' WHERE transaction_no ilike 'Stock%' and initcap(store_name) = '"+storeNamecmbx.getSelectedItem().toString()+"' and trans_date::date ='"+dateCmbx.getSelectedItem().toString()+"' ");
            updsubStores.executeUpdate();
            
            PreparedStatement updCount = connectDB.prepareStatement("update st_stock_count set  input_date='"+SQLDateFormat.getSQLDate(datePicker2.getDate())+"|| "+time+"' WHERE department ilike '"+storeNamecmbx.getSelectedItem().toString()+"' and input_date::date = '"+dateCmbx.getSelectedItem().toString()+"' ");
            updCount.executeUpdate();
            
            
            JOptionPane.showMessageDialog(this, "Done!");
            
            
        } catch (SQLException ex) {
            Logger.getLogger(stockTakedates.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
        // Add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed
    

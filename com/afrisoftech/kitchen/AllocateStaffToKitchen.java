         String[][] dataToSave = new String[6][2];
        dataToSave[0][0] = this.uuid;
        dataToSave[0][1] = "1"; //Change to the id of the user who is currently logged in.
        dataToSave[1][0] = "cboKitchen";
        dataToSave[1][1] = cboKitchen.getSelectedItem().toString();
        dataToSave[2][0] = "txtRole";
        dataToSave[2][1] = txtRole.getText().toString();
        dataToSave[3][0] = "cboStaffname";
        dataToSave[3][1] = cboStaffname.getSelectedItem().toString();
        dataToSave[4][0] = "txtDatefrom";
        dataToSave[4][1] = this.date1.format(txtDatefrom.getDate());
        dataToSave[5][0] = "txtDateto";
        dataToSave[5][1] = this.date1.format(txtDateto.getDate());
      
        myConn = connectDB;
        saveApplication(dataToSave);
        txtRole.setText("");
        cboKitchen.setSelectedIndex(0);
        cboStaffname.setSelectedIndex(0);
        txtDatefrom.setDateFormatString("");
        txtDateto.setDateFormatString("");
        tblReport = loadData(tblReport, this.uuid, "tblReport");

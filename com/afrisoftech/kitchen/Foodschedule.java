    private void cboFoodItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboFoodItemActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            myConn = connectDB;
            java.sql.Statement str = connectDB.createStatement();
            ResultSet rs = str.executeQuery("SELECT buying_price,units FROM st_stock_item WHERE broad_category = 'FOODSTUFFS' AND description='"+cboFoodItem.getSelectedItem()+"'");
            if(rs.next()){
                jLabel16.setText("Quantity in "+rs.getObject(2).toString()+" : ");
                units = rs.getObject(2).toString();
            }
        } catch (SQLException ex) {
            Logger.getLogger(ApproveRequisitionForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cboFoodItemActionPerformed


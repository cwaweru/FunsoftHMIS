    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                if (jTable1.getValueAt(i, 0) != null) {
                    java.sql.PreparedStatement pstmt = connectDB.prepareStatement("UPDATE pb_operating_parameters set main_service = ?, gl_account = ? where service_type ilike ? and main_service ilike ? and code ilike ?");
                    pstmt.setString(1, jComboBox2.getSelectedItem().toString());
                    pstmt.setString(2, com.afrisoftech.lib.GLCodesFactory.getGlCode(connectDB, jComboBox2.getSelectedItem().toString()));
                    pstmt.setString(3, jTable1.getValueAt(i, 0).toString());
                    pstmt.setString(4, jComboBox1.getSelectedItem().toString());
                    pstmt.setString(5, jTable1.getValueAt(i, 2).toString());
                    pstmt.executeUpdate();
                    //javax.swing.JOptionPane.showMessageDialog(this, "Service charter updated successfully.");
                }
            }
            javax.swing.JOptionPane.showMessageDialog(this, "Service charter updated successfully.");

            // Add your handling code here:
        } catch (SQLException ex) {
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(ServiceCharterAdjustmentIntfr.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


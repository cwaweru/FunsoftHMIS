    private void jSearchTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jSearchTable1MouseClicked
        bodyNumberTxt.setText(jSearchTable1.getModel().getValueAt(jSearchTable1.getSelectedRow(), 0).toString());

        try {
            java.sql.Statement stmt = connectDB.createStatement();
            java.sql.ResultSet rset = stmt.executeQuery("select patient_name,unit_category,unit_no,year_of_birth,gender from hp_mortuary where annual_no ='" + jSearchTable1.getValueAt(jSearchTable1.getSelectedRow(), 0).toString() + "' AND discharged = false");
            while (rset.next()) {
                bodyNameTxt.setText(rset.getObject(1).toString());
                currentCategoryTxt.setText(rset.getObject(2).toString());
                currentUnitNumberTxt.setText(rset.getObject(3).toString());
                ageTxt.setText(rset.getObject(4).toString());
                genderTxt.setText(rset.getObject(5).toString());
            }
        } catch (java.sql.SQLException sqe) {
            sqe.printStackTrace();
            System.out.println("Insert not successful");
        }
        jSearchDialog1.dispose();
        // Add your handling code here:
    }//GEN-LAST:event_jSearchTable1MouseClicked


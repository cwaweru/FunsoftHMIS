    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            connectDB.setAutoCommit(false);
            java.sql.PreparedStatement pstmt = connectDB.prepareStatement("INSERT INTO hp_mort_category_transfer("
                    + "            body_no, body_name, transfered_from, source_category, new_category, "
                    + "            cat_code, source_unit_no, new_unit_no, unit_category, "
                    + "            reason_for_transfer)"
                    + "    VALUES (?, ?, ?, ?, ?, "
                    + "            ?, ?, ?, ?, ? "
                    + "            )");
            pstmt.setString(1, bodyNumberTxt.getText());
            pstmt.setString(2, bodyNameTxt.getText());
            pstmt.setString(3, currentUnitNumberTxt.getText());
            pstmt.setString(4, currentCategoryTxt.getText());
            pstmt.setString(5, targetCategoryCmbx.getSelectedItem().toString());
            pstmt.setString(6, targetCategoryTxt.getText());
            pstmt.setString(7, currentUnitNumberTxt.getText());
            pstmt.setString(8, targetUnitNumberCmbx.getSelectedItem().toString());
            pstmt.setString(9, targetCategoryCmbx.getSelectedItem().toString());
            pstmt.setString(10, reasonTextPanel.getText());

            pstmt.executeUpdate();

            java.sql.PreparedStatement pstmt22 = connectDB.prepareStatement("UPDATE hp_mort_units_inventory SET unit_occupied = 'false' where unit_no ='" + currentUnitNumberTxt.getText() + "' and unit_category = '" + currentCategoryTxt.getText() + "'");
            pstmt22.executeUpdate();


            java.sql.PreparedStatement pstmt11 = connectDB.prepareStatement("update hp_mort_units_inventory set unit_occupied = 'true' where unit_no ='" + targetUnitNumberCmbx.getSelectedItem() + "' AND unit_category = '" + targetCategoryCmbx.getSelectedItem() + "'");

            pstmt11.executeUpdate();

            java.sql.PreparedStatement pstmt12 = connectDB.prepareStatement("update hp_mortuary set  "
                    + "unit_no  = '" + targetUnitNumberCmbx.getSelectedItem().toString() + "',unit_category ='" + targetCategoryCmbx.getSelectedItem().toString() + "',"
                    + "unit_rate = '" + Double.parseDouble(newCategoryRateTxt.getText()) + "'  WHERE  patient_no = '" + bodyNumberTxt.getText() + "' AND discharged = false");

            pstmt12.executeUpdate();
            connectDB.commit();
            connectDB.setAutoCommit(true);

            javax.swing.JOptionPane.showMessageDialog(this, "Insert Done Successfully", "Comfirmation Message", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        } catch (java.sql.SQLException sq) {
            sq.printStackTrace();
            System.out.println(sq.getMessage());
            try {
                connectDB.rollback();
            } catch (java.sql.SQLException sql) {
                sql.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(this, sql.getMessage(), "Error Message!", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
            jLabel12.setForeground(java.awt.Color.red);
            jLabel12.setText("Sorry. Transfer not effected. Please double check your entries");
        }


        // Add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed


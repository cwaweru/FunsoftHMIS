    private void targetCategoryCmbxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_targetCategoryCmbxActionPerformed

        try {
            java.sql.Statement stmt = connectDB.createStatement();
            java.sql.ResultSet rset = stmt.executeQuery("select category_name, normal_rate from hp_mort_category where category_name = '"+targetCategoryCmbx.getSelectedItem().toString()+"' order by category_name");
            while (rset.next()) {
                targetCategoryTxt.setText(rset.getObject(1).toString());
                newCategoryRateTxt.setText(String.valueOf(rset.getDouble(2)));
            }
        } catch (java.sql.SQLException sqe) {
            sqe.printStackTrace();
            System.out.println("Insert not successful");
        }


        targetUnitNumberCmbx.setModel(com.afrisoftech.lib.ComboBoxModel.ComboBoxModel(connectDB, "select unit_no from hp_mort_units_inventory where unit_category ILIKE '" + targetCategoryCmbx.getSelectedItem() + "%' and unit_occupied = 'f' order by unit_no asc"));

    }//GEN-LAST:event_targetCategoryCmbxActionPerformed


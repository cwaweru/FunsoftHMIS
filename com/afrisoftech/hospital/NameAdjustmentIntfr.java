                    java.sql.PreparedStatement pstmtNameChange5 = connectDB.prepareStatement("UPDATE hp_inpatient_register SET first_name = TRIM(INITCAP(?)), second_name = TRIM(INITCAP(?)), last_name = TRIM(INITCAP(?)) WHERE patient_no = ?");

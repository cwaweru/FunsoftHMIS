    private void savejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savejButtonActionPerformed
        // TODO add your handling code here:
        
        if(vehicleRegjTextField.getText().length()<1 ||
                    kmInjTextField.getText().length()<1||
                        kmOutjTextField.getText().length()<1||
                            defectsjTextArea.getText().length()<1||
                                toolsjTextArea.getText().length()<1||
                                    wheeljTextField.getText().length()<1){
            javax.swing.JOptionPane.showMessageDialog(this, "Please ensure all the fields have valid data.",
                    "Caution message!",javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
            
                java.text.SimpleDateFormat sdf=new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                java.lang.String requestID="MVR_";
            
                connectDB.setAutoCommit(false);
                
                java.sql.Statement idStt=connectDB.createStatement();
                java.sql.ResultSet idRSet=idStt.executeQuery("SELECT nextval('maintenance.mvehicle_repair_id_seq')");
                
                while(idRSet.next()){
                    requestID+=idRSet.getString(1);
                }
            
                java.sql.PreparedStatement savePs=connectDB.prepareStatement("INSERT INTO maintenance.motor_vehicle_requests VALUES (?,?,?,?,?,?,?,?,?)");
                savePs.setString(1, requestID);
                savePs.setString(2, vehicleRegjTextField.getText().toUpperCase());
                savePs.setString(3, kmInjTextField.getText());
                savePs.setString(4, kmOutjTextField.getText());
                savePs.setString(5, defectsjTextArea.getText());
                savePs.setString(6, toolsjTextArea.getText());
                savePs.setString(7, wheeljTextField.getText());
                savePs.setObject(8, java.sql.Timestamp.valueOf(sdf.format(new java.util.Date())));
                savePs.setString(9, currentUsrjTextField.getText());
                
                savePs.executeUpdate();
                connectDB.commit();
                connectDB.setAutoCommit(true);
                
                javax.swing.JOptionPane.showMessageDialog(this, "Insert Successful. Motor Vehicle Repair Request No: "+requestID,
                    "Insert Successful!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
                
                clearjButton.doClick();
            
            }
            catch(java.sql.SQLException sqle){
                sqle.printStackTrace();
                
                try{
                    connectDB.rollback();
                    connectDB.setAutoCommit(true);
                }
                catch(java.sql.SQLException sqlex){
                    sqlex.printStackTrace();
                }
            }
        }
        
        
        
    }//GEN-LAST:event_savejButtonActionPerformed


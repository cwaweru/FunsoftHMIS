    private void e1SearchTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_e1SearchTableMouseClicked
        // Add your handling code here:
        e1NumberTextField.setText(e1SearchTable.getValueAt(e1SearchTable.getSelectedRow(), 0).toString());
        
        try{
            
            java.sql.Statement detailsStt=connectDB.createStatement();
            java.sql.ResultSet detailsRs=detailsStt.executeQuery("SELECT department,building,floor,room,urgency,subject,phone_ext,description,user_name FROM maintenance.e1_details WHERE e1_id='"+e1NumberTextField.getText()+"' AND e1_id IN (SELECT e1_id FROM maintenance.e1_approved)");
            
            while(detailsRs.next()){
                deptjTextField.setText(detailsRs.getString(1));
                buildjTextField.setText(detailsRs.getString(2));
                floorTextField.setText(detailsRs.getString(3));
                roomTextField.setText(detailsRs.getString(4));
                urgencyjTextField.setText(detailsRs.getString(5));
                subjectTextField.setText(detailsRs.getString(6));
                phoneTextField.setText(detailsRs.getString(7));
                descriptionArea.setText(detailsRs.getString(8));
                e1CreatorTextField.setText(detailsRs.getString(9));
            }
            
            detailsStt=null;
            detailsRs=null;
            
            detailsStt=connectDB.createStatement();
            detailsRs=detailsStt.executeQuery("SELECT user_name FROM maintenance.e1_approved WHERE e1_id='"+e1NumberTextField.getText()+"'");
            
            while(detailsRs.next()){
                e1ApproverTextField.setText(detailsRs.getString(1));
            }
            
        }
        catch(java.sql.SQLException sqle){
            sqle.printStackTrace();
        }
        
        if(!jButton1.isEnabled()){
            jButton1.setEnabled(true);
        }
        
        e1SearchDialog.dispose();
        
    }//GEN-LAST:event_e1SearchTableMouseClicked


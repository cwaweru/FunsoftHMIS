    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(assignjComboBox.getSelectedItem().toString().equalsIgnoreCase("-")||
                assignjComboBox.getSelectedItem()==null){
            javax.swing.JOptionPane.showMessageDialog(this, "Please first assign this E1 Document to an appropriate section.",
                    "Caution Message",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else if(nameOfficersTextField.getText().length()<2){
            javax.swing.JOptionPane.showMessageDialog(this, "Please specify the names of the officers handling this E1",
                    "Caution Message",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            
            try{
                
                java.text.SimpleDateFormat sdf=new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                
                connectDB.setAutoCommit(false);
                
                
                java.sql.PreparedStatement registerPrepStt=connectDB.prepareStatement("UPDATE maintenance.e1_details SET registered=true WHERE e1_id='"+e1NumberTextField.getText()+"'");
                registerPrepStt.executeUpdate();
                
                
                //random numbers give a temporary job card no before the update with the nextval just before commit
                //concatenated 2 random numbers to further decrease the likelihood of 2 jar files generating the same number in this form
                //done to minimize what appears to be skipping of job card no's when/if exceptions occur
                double random1=(double)java.lang.Math.random()*10000;
                double random2=(double)java.lang.Math.random()*10000;
                java.lang.StringBuffer tempJC=new java.lang.StringBuffer();
                tempJC.append(random1+"_"+random2+"");
            
                //replacing inherent period characters with zeroes to prevent DB exceptions
                int index1=tempJC.indexOf(".");
                int index2=tempJC.lastIndexOf(".");
                tempJC.setCharAt(index2, '0');
                tempJC.setCharAt(index1, '0');
                System.err.println("temporary jobCard "+tempJC.toString());
                
                
                java.sql.PreparedStatement jobCardRegPrepStt=connectDB.prepareCall("INSERT INTO maintenance.job_card_details VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
                jobCardRegPrepStt.setString(1, tempJC.toString());
                jobCardRegPrepStt.setString(2, e1NumberTextField.getText());
                jobCardRegPrepStt.setObject(3, null);//no expected duration yet
                jobCardRegPrepStt.setObject(4,assignjComboBox.getSelectedItem());
                jobCardRegPrepStt.setObject(5, null);//cant tell whether s11 used or not yet
                jobCardRegPrepStt.setString(6, null);// nameOfficersTextField.getText()); - officer names not determined at this point
                jobCardRegPrepStt.setString(7, null);//no irq job number yet
                jobCardRegPrepStt.setObject(8, null);//time instructed not yet determined
                jobCardRegPrepStt.setString(9, null);//actual duration not yet determined
                jobCardRegPrepStt.setString(10, null);//reason work pending not yet determined
                jobCardRegPrepStt.setString(11, null);//completion_worker not yet determined
                jobCardRegPrepStt.setString(12, null);//satisfaction level not yet determined
                jobCardRegPrepStt.setString(13, null);//sender completion comments not yet determined
                jobCardRegPrepStt.setObject(14, null);//sender completion time not yet determined
                jobCardRegPrepStt.setString(15, null);//completion sender username not yet determined
                jobCardRegPrepStt.setString(16, null);//head completion remarks not yet determined
                jobCardRegPrepStt.setString(17, null);//completion maintenance username not yet determined
                jobCardRegPrepStt.setObject(18, java.sql.Timestamp.valueOf(sdf.format(new java.util.Date())));//date job card created not yet determined
                jobCardRegPrepStt.executeUpdate();
                
                String jobCardNo="JOB_";
                java.sql.Statement jobCardSeqStt=connectDB.createStatement();
                java.sql.ResultSet jobCardSeqRSet=jobCardSeqStt.executeQuery("SELECT nextval('maintenance.job_card_no_seq')");
                
                while(jobCardSeqRSet.next()){
                    jobCardNo+=jobCardSeqRSet.getString(1);
                }
                
                java.sql.PreparedStatement updatePs=connectDB.prepareStatement("UPDATE maintenance.job_card_details SET job_card_no='"+jobCardNo+"' WHERE job_card_no='"+tempJC+"'");
                updatePs.executeUpdate();
                
                tempJC=null;
                
                //save and clean up
                connectDB.commit();
                connectDB.setAutoCommit(true);
                
                //notification
                javax.swing.JOptionPane.showMessageDialog(this, "E1 number:  "+e1NumberTextField.getText()+" has been registered.");
                
                 //clear
                jButton2.doClick();
                
                refreshTableApprovedAwaiting(awaitingRegjTable);
                
            }//ends try
            catch(java.sql.SQLException sqle){
                sqle.printStackTrace();
                
                javax.swing.JOptionPane.showMessageDialog(this, sqle,
                        "Error Message!",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
                
                try{
                    connectDB.rollback();
                    connectDB.setAutoCommit(true);
                }
                catch(java.sql.SQLException sqlex){
                    sqlex.printStackTrace();
                    
                    javax.swing.JOptionPane.showMessageDialog(this, sqlex,
                        "Error Message!",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
                }
            }
            
                    
            //disable save
            jButton1.setEnabled(false);
            
            //clear fields
            jButton2.doClick();
            
        }


    }//GEN-LAST:event_jButton1ActionPerformed


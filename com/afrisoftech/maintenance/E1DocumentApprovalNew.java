    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            try{
                
                
                
                connectDB.setAutoCommit(false);
                
                
                java.sql.PreparedStatement updatePrepStt=connectDB.prepareStatement("UPDATE maintenance.e1_details SET approved=true WHERE e1_id='"+e1NumberTextField.getText()+"'");
                updatePrepStt.executeUpdate();
                
                java.sql.PreparedStatement approvePrepStt=connectDB.prepareCall("INSERT INTO maintenance.e1_approved VALUES (?,?)");
                approvePrepStt.setString(1, e1NumberTextField.getText());
                approvePrepStt.setString(2, e1CreatorTextField.getText());
                //approvePrepStt.setObject(3, java.sql.Timestamp.valueOf(sdf.format(new java.util.Date())));
                approvePrepStt.executeUpdate();
                
                //save and clean up
                connectDB.commit();
                connectDB.setAutoCommit(true);
                
                //notification
                javax.swing.JOptionPane.showMessageDialog(this, "E1 number  "+e1NumberTextField.getText()+" has been approved.");
                
                 //clear
                jButton2.doClick();
                
                refreshTableApprovedAwaiting(awaitingjTable);
                
            }//ends try
            catch(java.sql.SQLException sqle){
                sqle.printStackTrace();
                
                try{
                    connectDB.rollback();
                    connectDB.setAutoCommit(true);
                }
                catch(java.sql.SQLException sqlex){
                    sqlex.printStackTrace();
                }
            }
            
                    
            //disable save
            jButton1.setEnabled(false);
            
            //clear fields
            jButton4.doClick();
           
        


    }//GEN-LAST:event_jButton1ActionPerformed


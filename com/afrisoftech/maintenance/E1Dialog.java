    private void printjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printjButtonActionPerformed
        // TODO add your handling code here:
        
        
        if(jComboBox1.getSelectedItem().toString().equalsIgnoreCase("-")){
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a valid department!",
                    "Error Message!",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        } else{
            
            java.text.SimpleDateFormat sdf=new java.text.SimpleDateFormat("yyyy-MM-dd");// HH:mm:ss");
            
            switch(reportType){
            
            
                case 1:{
                    
                    com.afrisoftech.maintenance.E1PerDepartmentPdf e1pd=new com.afrisoftech.maintenance.E1PerDepartmentPdf();
            
                    e1pd.E1PerDepartmentPdf(connectDB, jComboBox1.getSelectedItem().toString(),sdf.format(begindatePicker.getDate()),sdf.format(enddatePicker.getDate()));
                }break;
                    
                case 2:{
                    com.afrisoftech.maintenance.E1ApprovedPerDepartmentPdf e1approvedpd=new com.afrisoftech.maintenance.E1ApprovedPerDepartmentPdf();
            
                    e1approvedpd.E1ApprovedPerDepartmentPdf(connectDB, jComboBox1.getSelectedItem().toString(),sdf.format(begindatePicker.getDate()),sdf.format(enddatePicker.getDate()));
                }break;
                    
                case 3:{
                    
                    if(jComboBox1.getSelectedItem().toString().equalsIgnoreCase("All Departments")){
                        com.afrisoftech.maintenance.E1RegisteredAllDepartmentsPdf e1RgstAll=new com.afrisoftech.maintenance.E1RegisteredAllDepartmentsPdf();
            
                        e1RgstAll.E1RegisteredAllDepartmentsPdf(connectDB, sdf.format(begindatePicker.getDate()),sdf.format(enddatePicker.getDate()));
            
                    }else{
                        com.afrisoftech.maintenance.E1RegisteredPerDepartmentPdf e1RegPdp=new com.afrisoftech.maintenance.E1RegisteredPerDepartmentPdf();
            
                        e1RegPdp.E1RegisteredPerDepartmentPdf(connectDB, jComboBox1.getSelectedItem().toString(),sdf.format(begindatePicker.getDate()),sdf.format(enddatePicker.getDate()));
                    }
                    
                    
                    
                }break;
                case 4:{
                    com.afrisoftech.maintenance.E1RegisteredAllDepartmentsPdf e1RegAll=new com.afrisoftech.maintenance.E1RegisteredAllDepartmentsPdf();
            
                    e1RegAll.E1RegisteredAllDepartmentsPdf(connectDB, sdf.format(begindatePicker.getDate()),sdf.format(enddatePicker.getDate()));
                    
                }break;
                    
                default:;
           
            
            }
            
            
        };

    }//GEN-LAST:event_printjButtonActionPerformed

    private void searchjTextFieldCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_searchjTextFieldCaretUpdate
        // Add your handling code here:
        if(searchjTextField.getCaretPosition()>2){
            jSearchTable.setModel(com.afrisoftech.dbadmin.TableModel.createTableVectors(connectDB,"SELECT job_card_no,section_assigned FROM maintenance.job_card_details "
                + "WHERE job_card_no ILIKE '"+searchjTextField.getText()+"%' "
                + "AND completion_worker_username IS NOT NULL "
                + "AND completion_sender_username IS NOT NULL "
                + "AND completion_maintenance_username IS NOT NULL "
                + "ORDER BY job_card_no"));
        }

    }//GEN-LAST:event_searchjTextFieldCaretUpdate


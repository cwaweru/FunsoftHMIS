    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String selected_id = tblReport.getValueAt(tblReport.getSelectedRow(), 0).toString();
        String[][] dataToSave = new String[3][2];
        dataToSave[0][0] = "-1";// when want to update
        dataToSave[0][1] = selected_id; //Change to the id of the user who is currently logged in.
        dataToSave[1][0] = "cboMaintenanceDepartments";
        dataToSave[1][1] = cboMaintenanceDepartments.getSelectedItem().toString();
        dataToSave[2][0] = "txtMaintenanceOfficial";
        dataToSave[2][1] = txtMaintenanceOfficial.getText().toString();
        System.out.println("txtMaintenanceOfficial: " + txtMaintenanceOfficial.getText());
        saveApplication(dataToSave);
        approveApplication(Integer.parseInt(selected_id), getUuidNo(), true);
            
        tblReport = loadData(tblReport, this.uuid, "tblReport");
        JOptionPane.showMessageDialog(null, "This Application: " + lblSubject.getText().toString() + " has been Recorded under Official Name: " + txtMaintenanceOfficial.getText().toString());
        txaDescription.setText("");
        lblDepartment.setText("");
        lblBuilding.setText("");
        txaDescription.setText("");
        lblFloor.setText("");
        lblPhoneext.setText("");
        lblRoom.setText("");
        lblSubject.setText("");
        lblUrgent.setText("");
        txtMaintenanceOfficial.setText("");
        saveBtn.setEnabled(false);
    }//GEN-LAST:event_saveBtnActionPerformed


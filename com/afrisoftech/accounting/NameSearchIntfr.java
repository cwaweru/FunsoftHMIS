    private void searchInformationTxtCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_searchInformationTxtCaretUpdate
      if(jCheckBox2.isSelected()){
          if (searchInformationTxt.getCaretPosition() > 2){
          patientDataTable.setModel(com.afrisoftech.dbadmin.TableModel.createTableVectors(connectDB, "SELECT patient_no as hospital_no , sub_chief as unit_no, first_name || ' '|| second_name || ' ' || last_name as patient_name ,specialty_clinic as clinic , residence,  tel_no as telephone ,  id_no, date FROM hp_patient_register where"
                    + " patient_no ilike '%" + searchInformationTxt.getText() + "%' or first_name || ' '|| second_name || ' ' || last_name ilike '%" + searchInformationTxt.getText() + "%'"
                    + " or sub_chief ilike '%" + searchInformationTxt.getText() + "%'  or tel_no ilike '%"
                    + searchInformationTxt.getText() + "%' or id_no ilike '%" + searchInformationTxt.getText() + "%' "));
      }
          
      }else if (searchInformationTxt.getCaretPosition() > 2) {
            patientDataTable.setModel(com.afrisoftech.dbadmin.TableModel.createTableVectors(connectDB, "SELECT patient_no as hospital_number,"
                    + " sub_chief as unit_number, patient_name, ward, residence, tel as telephone, id_no, date_admitted as admission_date from hp_admission where "
                    + " patient_no ilike '%" + searchInformationTxt.getText() + "%' or patient_name ilike '%" + searchInformationTxt.getText() + "%'"
                    + " or sub_chief ilike '%" + searchInformationTxt.getText() + "%' and check_out = false or tel ilike '%"
                    + searchInformationTxt.getText() + "%' or id_no ilike '%" + searchInformationTxt.getText() + "%' "));
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_searchInformationTxtCaretUpdate

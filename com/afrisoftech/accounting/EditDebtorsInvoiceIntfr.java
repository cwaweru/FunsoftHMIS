    private void closeFormButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_closeFormButtonActionPerformed
        try {
            java.sql.PreparedStatement pstmtEditInvoice = connectDB.prepareStatement("UPDATE ac_debtors SET reason = ? WHERE invoice_no = ?");
            pstmtEditInvoice.setString(1, updatedInvoiceDetailsTxt.getText());
            pstmtEditInvoice.setString(2, invoiceNumberTxt.getText());
            pstmtEditInvoice.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Invoice Number  : [" + invoiceNumberTxt.getText() + "] updated successfully.");
            com.afrisoftech.reports.FinalDebtInvoicePdf policy = new com.afrisoftech.reports.FinalDebtInvoicePdf();
            policy.FinalDebtInvoicePdf(connectDB, invoiceNumberTxt.getText());
            // TODO add your handling code here:
        } catch (SQLException ex) {
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(EditDebtorsInvoiceIntfr.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_closeFormButtonActionPerformed

    public void populateTable(){
        batch = new Object[0][4];
        RefreshClientsTable();
        try {
            //here
            String selectedDate=sdf2.format(datePicker.getDate());
            String sql="select st_stock_item.item_code, st_stock_item.description, st_stock_item.buying_price, condemnedlinen.qty\n" +
                        "from st_stock_item, condemnedlinen\n" +
                        "where st_stock_item.item_code=condemnedlinen.itemcode AND condemnedlinen.datefor like '"+selectedDate+"'\n" +
                        "AND st_stock_item.sub_cat_code like 'LINEN'\n" +
                        "order by st_stock_item.description asc";
            Statement stmt = conn.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,
ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = stmt.executeQuery( sql );
            rs.last();
            totalNumRows=rs.getRow();
            batch = new Object[totalNumRows][5];
            int r;
            r=0;
            rs.beforeFirst();
            while (rs.next()){
                batch[r][0]=rs.getString("description");
                batch[r][1]=rs.getString("qty");
                batch[r][2]=rs.getString("buying_price");
                Double totalVal=rs.getDouble("qty")*rs.getDouble("buying_price");
                batch[r][3]=totalVal;
                r++;
                RefreshClientsTable();
            }
            

        }catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Error: "+ex.toString());

       }
    }
